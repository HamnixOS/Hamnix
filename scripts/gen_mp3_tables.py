def emit(f):
    w = f.write
    w("# lib/mp3tables.ad — GENERATED by scripts/gen_mp3_tables.py. DO NOT EDIT.\n")
    w("#\n")
    w("# Fixed numeric data for the native MPEG-1 Layer III decoder\n")
    w("# (lib/mp3decode.ad): the packed Huffman decode trees, the synthesis\n")
    w("# dewindow, alias coefficients and the ISO band/bitrate tables. Exposed\n")
    w("# through accessor functions (Adder cannot import an array-typed global\n")
    w("# across modules, and has no array-literal initializer). Call mp3t_init()\n")
    w("# once before decoding.\n\n")

    w("g_ht:      Array[%d, uint16]\n" % len(G_HT))
    w("g_dtbl:    Array[%d, float64]\n" % len(G_DTBL))
